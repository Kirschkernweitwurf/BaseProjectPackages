#if UNITY_EDITOR
using System.Collections.Generic;
using System.Globalization;
using System.IO;
using System.Text;
using Base.UtilityPackage.Logging;
using UnityEditor;

namespace Base.ToolPackage.Editor.OrderManagement
{
    /// <summary>Writes the generated constants file from an <see cref="OrderRegistry"/>.</summary>
    public static class OrderCodeGenerator
    {
        private const string FileExtension = ".cs";
        private const string Indent = "    ";

        /// <summary>Generates the constants file and refreshes the AssetDatabase.</summary>
        public static void Generate(OrderRegistry registry)
        {
            if (registry == null)
                return;

            if (registry.Constants.Count == 0)
            {
                CustomLogger.LogWarning("Order Manager: no constants defined, generation skipped.", null);
                return;
            }

            string code = BuildCode(registry, out int count);
            string fileName = registry.RootClassName + FileExtension;
            string fullPath = Path.GetFullPath(Path.Combine(registry.OutputDirectory, fileName));
            string directory = Path.GetDirectoryName(fullPath);

            if (!string.IsNullOrEmpty(directory) && !Directory.Exists(directory))
                Directory.CreateDirectory(directory);

            File.WriteAllText(fullPath, code, Encoding.UTF8);
            AssetDatabase.Refresh();

            CustomLogger.Log($"Order Manager: generated {count} constant(s) to {fullPath}.", null);
        }

        private static string BuildCode(OrderRegistry registry, out int count)
        {
            StringBuilder builder = new();
            builder.AppendLine("// <auto-generated>");
            builder.AppendLine("// Generated by the Order Manager. Do not edit by hand.");
            builder.AppendLine("// </auto-generated>");
            builder.AppendLine();
            builder.Append("namespace ").AppendLine(registry.GeneratedNamespace);
            builder.AppendLine("{");
            builder.Append(Indent).Append("public static class ").AppendLine(registry.RootClassName);
            builder.Append(Indent).AppendLine("{");

            count = AppendConstants(builder, registry);

            builder.Append(Indent).AppendLine("}");
            builder.AppendLine("}");
            return builder.ToString();
        }

        private static int AppendConstants(StringBuilder builder, OrderRegistry registry)
        {
            List<OrderConstant> sorted = new(registry.Constants);
            sorted.Sort(CompareConstants);

            HashSet<string> usedNames = new();
            string lineIndent = Indent + Indent;
            int count = 0;

            foreach (OrderConstant constant in sorted)
            {
                string constantName = ToValidIdentifier(constant.Name);

                if (string.IsNullOrEmpty(constantName))
                    continue;

                if (!usedNames.Add(constantName))
                {
                    CustomLogger.LogWarning($"Order Manager: duplicate constant name '{constantName}' skipped.", null);
                    continue;
                }

                if (count > 0)
                    builder.AppendLine();

                AppendXmlSummary(builder, lineIndent, constant.Comment);
                builder.Append(lineIndent)
                    .Append("public const int ")
                    .Append(constantName)
                    .Append(" = ")
                    .Append(constant.Value.ToString(CultureInfo.InvariantCulture))
                    .AppendLine(";");

                count++;
            }

            return count;
        }

        private static void AppendXmlSummary(StringBuilder builder, string indent, string comment)
        {
            if (string.IsNullOrWhiteSpace(comment))
                return;

            string[] lines = Escape(comment).Split('\n');

            if (lines.Length == 1)
            {
                builder.Append(indent).Append("/// <summary>").Append(lines[0].Trim()).AppendLine("</summary>");
                return;
            }

            builder.Append(indent).AppendLine("/// <summary>");

            foreach (string line in lines)
                builder.Append(indent).Append("/// ").AppendLine(line.TrimEnd());

            builder.Append(indent).AppendLine("/// </summary>");
        }

        private static int CompareConstants(OrderConstant left, OrderConstant right)
        {
            int byValue = left.Value.CompareTo(right.Value);
            return byValue != 0
                ? byValue
                : string.CompareOrdinal(left.Name, right.Name);
        }

        private static string Escape(string text) => text.Replace("&", "&amp;")
            .Replace("<", "&lt;")
            .Replace(">", "&gt;")
            .Replace("\r", string.Empty);

        private static string ToValidIdentifier(string raw)
        {
            if (string.IsNullOrWhiteSpace(raw))
                return string.Empty;

            StringBuilder builder = new(raw.Length);

            foreach (char symbol in raw)
            {
                if (char.IsLetterOrDigit(symbol))
                    builder.Append(symbol);
            }

            if (builder.Length == 0)
                return string.Empty;

            if (char.IsDigit(builder[0]))
                builder.Insert(0, '_');

            return builder.ToString();
        }
    }
}
#endif